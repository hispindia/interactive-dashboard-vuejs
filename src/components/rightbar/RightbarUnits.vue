<template>
    <!-- Units toggle bar start-->
    <!-- <div class="row justify-content-center m-2">
      <div class="class"> -->
    <div class="row justify-content-center p-1 rightbarunit rightbar-menu-main">
        <div class="class col rightbar-menu">
            Unit : &ensp; &ensp;
            <input
                    v-model="selected"
                    type="radio"
                    name="units"
                    autocomplete="off"
                    value="count"
                    class="countunit"
                    checked
            > Count
            <input
                    v-model="selected"
                    type="radio"
                    name="units"
                    class="rateunit"
                    autocomplete="off"
                    value="rate"
            > Rate
            <input
                    v-model="selected"
                    type="radio"
                    name="units"
                    class="percentunit"
                    autocomplete="off"
                    value="percent"
            > % age
        </div>
    </div>

    <!-- Units toggle bar end-->
</template>
<script>
    import { EventBus } from "../../event-bus";
    import variables from "../../config.js";

    export default {
        name: "RightbarUnits",
        data() {
            return {
                selected: "count",
                chartType: "stackChart"
            };
        },
        mounted(){
            EventBus.$on("reset", this.reset);
            this.handleRightBar(this.chartType);
        },
        methods : {
            sendChart : function(){
                if (this.selected == "percent") {
                    EventBus.$emit("chartChange", "percent");
                } else if (this.selected == "count") {
                    EventBus.$emit("chartChange" , "count");
                } else { 
                    EventBus.$emit("chartChange" , "rate"); 
                }
            },
            reset : function(param){
                this.selected = 'count';
                this.chartType = param;
                this.handleRightBar(this.chartType);
                
            },
            handleRightBar: function(param) {
                switch (param) {
                    case "stackChart":
                        $(".percentunit").removeAttr("disabled");
                        $(".rateunit").removeAttr("disabled");
                        break;
                    case "mapChart":
                        $(".percentunit").attr("disabled", "disabled");
                        $(".rateunit").removeAttr("disabled");
                        break;
                    case "pointChart":
                        $(".percentunit").removeAttr("disabled");
                        $(".rateunit").removeAttr("disabled");
                        break;
                    case "pieChart":
                        break;
                    case "treeChart":
                        $(".percentunit").removeAttr("disabled");
                        $(".rateunit").attr("disabled", "disabled");
                        break;
                    case "heatChart":
                        break;
                    default:
                        break;
                        }            
                }
        },
        watch : {
            selected : function(){
                this.sendChart();
            }
        }
    };
</script>